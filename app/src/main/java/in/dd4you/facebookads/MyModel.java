public class MyModel {
    private String name, msg;

    public MyModel() {
    }

    public MyModel(String name, String msg) {
        this.name = name;
        this.msg = msg;
    }

    public String getName() {
        return name;
    }

    public String getMsg() {
        return msg;
    }
}
